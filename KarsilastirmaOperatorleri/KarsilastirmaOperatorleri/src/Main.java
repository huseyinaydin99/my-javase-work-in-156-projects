public class Main {
    public static void main(String[] args) {
        /*
        Karşılaştırma Operatörleri
        
        == ---> Eşit mi ? 
        != ---> Eşit Değil mi ?
        > ----> Büyük mü ?
        >= ---> Büyük veya Eşit mi ?
        < ----> Küçük mü ? 
        <= ----> Küçük veya Eşit mi ? 
        
        */
        System.out.println(3 == 4);
        System.out.println(3 != 4);
        System.out.println(2 > 1);
        System.out.println(2 >= 3);
        System.out.println(2 < 1);
        System.out.println(2 <= 1);
        System.out.println("Mehmet" == "Mehmet");
        
        
        
        
        
        
        
        
    
    }
}
